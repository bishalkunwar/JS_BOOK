// // Arrow Functions => Arrow Functions are the anonymous functions i.e. function without the name and without the identifier bounded.
// // Arrow functions do not return any value and can be declared without the function keyword.
// // Arrow functions are also called Lambda Functions.

// // Syntax:=> 
