// Arithmetic Operators: the operators that operate upon the numerical values and return a numerical value.

// Addition Operator
let add = 6+9;
console.log(add);

// Subtraction Operator
let sub = 10-7;
console.log(sub);

// Multiplication Operator
let result = 3*3;
console.log(result);

// Division Operator
let div = 2.0/2/0;
console.log(div);

// Modulas Operator
let rem = 9%5;
console.log(rem);

// Exponentian Operator
let pow = 2**3;
console.log(pow);

// Increment Operator
let g = 2;
let g1 = g++; 
console.log(g);

// Decrement Operator
let num1 = 5;
let num2 = num1--;
console.log(num1);

// Unary Plus Operator
let num6 = 24;
num7 = +num6;
console.log(num7); 

// Negation Operator
let num4 = 56;
num5 = -num4;
